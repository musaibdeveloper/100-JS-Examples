// JavaScript Program to Find the Square Root.

let num = 5;

console.log(num); // 5 

// To find the square root of the number is 

console.log(Math.sqrt(num)); //2.23606797749979
// It will give the result in points values. 

// Inorder to get the whole number we use Math.floor

console.log(Math.floor(Math.sqrt(num))); // 2

